# Main logger class
# ---------------------------------------------------------------------------

class PEVRLogger:
    """PEVR execution lifecycle logger.

    Tracks timing, errors, and structured data for each PEVR loop iteration.
    Designed as a context manager — ``__enter__`` logs the start,
    ``__exit__`` logs the end (or failure).

    Attributes:
        task: The user's original task text.
        session_id: Session identifier for correlation.
        loops: Completed loop records.
    """

    def __init__(self, task: str, session_id: str = ""):
        self.task = task
        self.session_id = session_id
        self.start_time: float = 0.0
        self.end_time: Optional[float] = None

        self.loops: List[LoopRecord] = []
        self._current_loop: Optional[LoopRecord] = None

        # Aggregated stats
        self.total_llm_calls: int = 0
        self.total_tool_calls: int = 0
        self.total_retries: int = 0
        self.total_prompt_tokens: int = 0
        self.total_completion_tokens: int = 0

        # Final result
        self.final_answer: Optional[str] = None
        self.final_error: Optional[str] = None
        self.finalization_tokens: TokenUsage = TokenUsage()

        # Config snapshot (set by orchestrator)
        self.config: Dict[str, Any] = {}

        # Custom events for learning integration tracking
        self._custom_events: List[Dict[str, Any]] = []

    # --- Context manager ---

    def __enter__(self) -> "PEVRLogger":
        self.start_time = time.time()
        logger.info(
            "[PEVR] === Session started === task=%s session=%s",
            self.task[:100],
            self.session_id,
        )
        return self

    def __exit__(self, exc_type, exc_val, exc_tb):
        self.end_time = time.time()
        self._finalize_loop()
        duration_ms = (self.end_time - self.start_time) * 1000

        if exc_type:
            self.final_error = str(exc_val)
            logger.error(
                "[PEVR] === Session FAILED === error=%s duration=%.0fms "
                "loops=%d llm_calls=%d tool_calls=%d "
                "tokens[prompt=%d completion=%d]",
                exc_val,
                duration_ms,
                len(self.loops),
                self.total_llm_calls,
                self.total_tool_calls,
                self.total_prompt_tokens,
                self.total_completion_tokens,
                exc_info=(exc_type, exc_val, exc_tb),
            )
        else:
            logger.info(
                "[PEVR] === Session completed === duration=%.0fms "
                "loops=%d retries=%d llm_calls=%d tool_calls=%d "
                "tokens[prompt=%d completion=%d] "
                "answer_len=%d",
                duration_ms,
                len(self.loops),
                self.total_retries,
                self.total_llm_calls,
                self.total_tool_calls,
                self.total_prompt_tokens,
                self.total_completion_tokens,
                len(self.final_answer) if self.final_answer else 0,
            )

        # Auto-save trace
        self._auto_save_trace()

    # --- Loop lifecycle ---

    def _finalize_loop(self):
        """Finalize current loop and append to loops list."""
        if self._current_loop is not None:
            self._current_loop.end_time = time.time()
            self._current_loop.end_ts = _ts()
            self.loops.append(self._current_loop)
            self._current_loop = None

    def _ensure_loop(self, loop_index: int):
        """Ensure an active loop record, finalizing previous if index changed."""
        if (
            self._current_loop is None
            or self._current_loop.loop_index != loop_index
        ):
            self._finalize_loop()
            self._current_loop = LoopRecord(
                loop_index=loop_index,
                start_time=time.time(),
                start_ts=_ts(),
            )

    # --- Phase logging ---

    def log_planning(
        self,
        loop_index: int,
        plan_steps: List[Dict[str, Any]],
        duration_s: float,
        error: Optional[Exception] = None,
        token_usage: Optional[Dict[str, int]] = None,
    ):
        """Log the planning phase."""
        self._ensure_loop(loop_index)
        self.total_llm_calls += 1
        loop = self._current_loop

        loop.planning_duration_ms = duration_s * 1000
        if token_usage:
            loop.planning_tokens = TokenUsage(
                prompt_tokens=token_usage.get("prompt_tokens", 0),
                completion_tokens=token_usage.get("completion_tokens", 0),
                total_tokens=token_usage.get("total_tokens", 0),
            )
            self.total_prompt_tokens += loop.planning_tokens.prompt_tokens
            self.total_completion_tokens += loop.planning_tokens.completion_tokens

        loop.plan_steps = [
            PlanStepRecord(
                step_id=s.get("id", ""),
                tool=s.get("tool", ""),
                description=s.get("description", ""),
                expected=s.get("expected", ""),
            )
            for s in plan_steps
        ]

        if error:
            loop.planning_error = str(error)
            logger.error(
                "[PEVR] [%s] Planning FAILED at loop %d: %s (%.0fms) "
                "tokens[prompt=%d completion=%d]",
                _ts(),
                loop_index,
                error,
                loop.planning_duration_ms,
                loop.planning_tokens.prompt_tokens,
                loop.planning_tokens.completion_tokens,
            )
        else:
            logger.info(
                "[PEVR] [%s] Planning loop %d: %d steps generated (%.0fms) "
                "tokens[prompt=%d completion=%d]",
                _ts(),
                loop_index,
                len(plan_steps),
                loop.planning_duration_ms,
                loop.planning_tokens.prompt_tokens,
                loop.planning_tokens.completion_tokens,
            )
            # Log each step at DEBUG level
            for step in loop.plan_steps:
                logger.debug(
                    "[PEVR]   step %s: tool=%s desc=%s",
                    step.step_id,
                    step.tool,
                    step.description[:80],
                )

    def log_execution(
        self,
        loop_index: int,
        observations: List[Dict[str, Any]],
        duration_s: float,
        error: Optional[Exception] = None,
        token_usage: Optional[Dict[str, int]] = None,
    ):
        """Log the execution phase."""
        self._ensure_loop(loop_index)
        loop = self._current_loop

        loop.execution_duration_ms = duration_s * 1000
        if token_usage:
            loop.execution_tokens = TokenUsage(
                prompt_tokens=token_usage.get("prompt_tokens", 0),
                completion_tokens=token_usage.get("completion_tokens", 0),
                total_tokens=token_usage.get("total_tokens", 0),
            )
            self.total_prompt_tokens += loop.execution_tokens.prompt_tokens
            self.total_completion_tokens += loop.execution_tokens.completion_tokens

        for obs in observations:
            tc = ToolCallRecord(
                step_id=obs.get("step_id", ""),
                tool_name=obs.get("tool", "unknown"),
                status=obs.get("status", "unknown"),
                error=obs.get("error"),
                output_preview=str(obs.get("result", ""))[:200],
            )
            loop.tool_calls.append(tc)
            self.total_tool_calls += 1

        success_count = sum(1 for o in observations if o.get("status") == "success")
        self.total_llm_calls += 1  # executor uses at least 1 LLM call

        if error:
            loop.execution_error = str(error)
            logger.error(
                "[PEVR] [%s] Execution FAILED at loop %d: %s (%.0fms) "
                "tokens[prompt=%d completion=%d]",
                _ts(),
                loop_index,
                error,
                loop.execution_duration_ms,
                loop.execution_tokens.prompt_tokens,
                loop.execution_tokens.completion_tokens,
            )
        else:
            logger.info(
                "[PEVR] [%s] Execution loop %d: %d/%d tools succeeded (%.0fms) "
                "tokens[prompt=%d completion=%d]",
                _ts(),
                loop_index,
                success_count,
                len(observations),
                loop.execution_duration_ms,
                loop.execution_tokens.prompt_tokens,
                loop.execution_tokens.completion_tokens,
            )
            # Log each tool call at DEBUG
            for tc in loop.tool_calls:
                if tc.status == "success":
                    logger.debug(
                        "[PEVR]   %s: %s OK (%.0fms) preview=%s",
                        tc.step_id,
                        tc.tool_name,
                        tc.duration_ms,
                        tc.output_preview[:80],
                    )
                else:
                    logger.warning(
                        "[PEVR]   %s: %s FAILED error=%s",
                        tc.step_id,
                        tc.tool_name,
                        tc.error or "unknown",
                    )

    def log_verification(
        self,
        loop_index: int,
        report: Dict[str, Any],
        duration_s: float,
        error: Optional[Exception] = None,
        token_usage: Optional[Dict[str, int]] = None,
    ):
        """Log the verification phase."""
        self._ensure_loop(loop_index)
        self.total_llm_calls += 1
        loop = self._current_loop

        loop.verification_duration_ms = duration_s * 1000
        if token_usage:
            loop.verification_tokens = TokenUsage(
                prompt_tokens=token_usage.get("prompt_tokens", 0),
                completion_tokens=token_usage.get("completion_tokens", 0),
                total_tokens=token_usage.get("total_tokens", 0),
            )
            self.total_prompt_tokens += loop.verification_tokens.prompt_tokens
            self.total_completion_tokens += loop.verification_tokens.completion_tokens

        loop.verification_passed = report.get("passed", False)
        loop.verification_coverage = float(report.get("coverage", 0.0))
        loop.verification_grounded = bool(report.get("grounded", False))
        loop.verification_reason = str(report.get("reason", ""))
        loop.verification_missing = list(report.get("missing", []))

        if error:
            loop.verification_error = str(error)
            logger.error(
                "[PEVR] [%s] Verification FAILED (error) at loop %d: %s (%.0fms) "
                "tokens[prompt=%d completion=%d]",
                _ts(),
                loop_index,
                error,
                loop.verification_duration_ms,
                loop.verification_tokens.prompt_tokens,
                loop.verification_tokens.completion_tokens,
            )
        else:
            verdict = "PASSED" if loop.verification_passed else "FAILED"
            logger.info(
                "[PEVR] [%s] Verification loop %d: %s coverage=%.2f grounded=%s (%.0fms) "
                "tokens[prompt=%d completion=%d]",
                _ts(),
                loop_index,
                verdict,
                loop.verification_coverage,
                loop.verification_grounded,
                loop.verification_duration_ms,
                loop.verification_tokens.prompt_tokens,
                loop.verification_tokens.completion_tokens,
            )
            if not loop.verification_passed:
                logger.info(
                    "[PEVR]   reason: %s",
                    loop.verification_reason[:200],
                )
                if loop.verification_missing:
                    logger.info(
                        "[PEVR]   missing: %s",
                        loop.verification_missing,
                    )

    def log_replanning(
        self,
        loop_index: int,
        action: str,
        target_step: Optional[str],
        new_steps_count: int,
        duration_s: float,
        error: Optional[Exception] = None,
        token_usage: Optional[Dict[str, int]] = None,
    ):
        """Log the replanning phase."""
        self._ensure_loop(loop_index)
        self.total_llm_calls += 1
        self.total_retries += 1
        loop = self._current_loop

        loop.replan_action = action
        loop.replan_target_step = target_step
        loop.replan_new_steps_count = new_steps_count
        loop.replan_duration_ms = duration_s * 1000
        if token_usage:
            loop.replan_tokens = TokenUsage(
                prompt_tokens=token_usage.get("prompt_tokens", 0),
                completion_tokens=token_usage.get("completion_tokens", 0),
                total_tokens=token_usage.get("total_tokens", 0),
            )
            self.total_prompt_tokens += loop.replan_tokens.prompt_tokens
            self.total_completion_tokens += loop.replan_tokens.completion_tokens

        if error:
            loop.replan_error = str(error)
            logger.error(
                "[PEVR] [%s] Replanning FAILED at loop %d: %s (%.0fms) "
                "tokens[prompt=%d completion=%d]",
                _ts(),
                loop_index,
                error,
                loop.replan_duration_ms,
                loop.replan_tokens.prompt_tokens,
                loop.replan_tokens.completion_tokens,
            )
        else:
            logger.info(
                "[PEVR] [%s] Replanning loop %d: action=%s target=%s new_steps=%d (%.0fms) "
                "tokens[prompt=%d completion=%d]",
                _ts(),
                loop_index,
                action,
                target_step,
                new_steps_count,
                loop.replan_duration_ms,
                loop.replan_tokens.prompt_tokens,
                loop.replan_tokens.completion_tokens,
            )

    def log_finalization(
        self,
        answer: str,
        duration_s: float,
        error: Optional[Exception] = None,
        token_usage: Optional[Dict[str, int]] = None,
    ):
        """Log the finalization phase."""
        self._finalize_loop()
        self.total_llm_calls += 1
        self.final_answer = answer
        if token_usage:
            self.finalization_tokens = TokenUsage(
                prompt_tokens=token_usage.get("prompt_tokens", 0),
                completion_tokens=token_usage.get("completion_tokens", 0),
                total_tokens=token_usage.get("total_tokens", 0),
            )
            self.total_prompt_tokens += self.finalization_tokens.prompt_tokens
            self.total_completion_tokens += self.finalization_tokens.completion_tokens

        if error:
            logger.error(
                "[PEVR] [%s] Finalization FAILED: %s (%.0fms) "
                "tokens[prompt=%d completion=%d]",
                _ts(),
                error,
                duration_s * 1000,
                self.finalization_tokens.prompt_tokens,
                self.finalization_tokens.completion_tokens,
            )
        else:
            logger.info(
                "[PEVR] [%s] Finalization: %d chars (%.0fms) "
                "tokens[prompt=%d completion=%d]",
                _ts(),
                len(answer),
                duration_s * 1000,
                self.finalization_tokens.prompt_tokens,
                self.finalization_tokens.completion_tokens,
            )

    def log_skill_policy(
        self,
        loop_index: int,
        report: Dict[str, Any],
        duration_ms: float,
    ):
        """Log the SkillPolicy compilation phase.

        Records MATCH/GATE/COMPILE/GUARD results for each skill referenced
        in the plan.  Called by skill_policy_node after processing.

        Args:
            loop_index: Current loop index (same as retry_count).
            report: The SkillPolicyReport dict from skill_policy_node.
            duration_ms: Processing duration in milliseconds.
        """
        self._ensure_loop(loop_index)
        loop = self._current_loop

        rec = SkillPolicyRecord(
            policy_applied=report.get("policy_applied", False),
            plan_steps_in=report.get("plan_steps_in", 0),
            skill_refs_found=report.get("skill_refs_found", 0),
            skill_refs=report.get("skill_refs", []),
            matched_count=len(report.get("matched_skills", [])),
            matched_skills=[
                {"name": m.get("skill_name"), "type": m.get("skill_type"), "score": m.get("score")}
                for m in report.get("matched_skills", [])
            ],
            gate_results=report.get("gate_results", []),
            guard_passed=report.get("guard_passed", False),
            guard_issues=report.get("guard_issues", []),
            compiled_count=report.get("compiled_count", 0),
            compiled_skills=report.get("compiled_skills", []),
            plan_steps_out=report.get("plan_steps_out", 0),
            duration_ms=round(duration_ms, 1),
            error=report.get("error"),
            feature_enabled=report.get("feature_enabled", True),
        )
        loop.skill_policy = rec

        if rec.error:
            logger.error(
                "[PEVR] [%s] SkillPolicy FAILED at loop %d: %s (%.0fms)",
                _ts(), loop_index, rec.error, rec.duration_ms,
            )
        elif not rec.feature_enabled:
            logger.info(
                "[PEVR] [%s] SkillPolicy SKIP (disabled) at loop %d",
                _ts(), loop_index,
            )
        elif not rec.policy_applied:
            logger.info(
                "[PEVR] [%s] SkillPolicy PASS_THROUGH at loop %d: "
                "refs=%d matched=%d (%.0fms)",
                _ts(), loop_index, rec.skill_refs_found,
                rec.matched_count, rec.duration_ms,
            )
        else:
            allowed = [g for g in rec.gate_results if g.get("allowed")]
            rejected = [g for g in rec.gate_results if not g.get("allowed")]
            logger.info(
                "[PEVR] [%s] SkillPolicy COMPILED at loop %d: "
                "refs=%d matched=%d gate=%d/%d guard=%s "
                "plan=%d→%d (%.0fms)",
                _ts(), loop_index,
                rec.skill_refs_found, rec.matched_count,
                len(allowed), len(allowed) + len(rejected),
                rec.guard_passed,
                rec.plan_steps_in, rec.plan_steps_out,
                rec.duration_ms,
            )
            for cs in rec.compiled_skills:
                logger.debug(
                    "[PEVR]   compiled: %s → %s",
                    cs.get("skill_name"), cs.get("compiled_tool"),
                )
            for g in rejected:
                logger.debug(
                    "[PEVR]   rejected: %s reason=%s",
                    g.get("skill_name"), g.get("reason"),
                )

    def log_error(self, phase: str, error: Exception, context: Dict[str, Any] = None):
        """Log an unexpected error at any phase.

        Args:
            phase: Which phase the error occurred in.
            error: The exception.
            context: Optional additional context dict.
        """
        logger.error(
            "[PEVR] Unexpected error in %s: %s context=%s",
            phase,
            error,
            context or {},
            exc_info=True,
        )

    def log_node_error(
        self,
        node_name: str,
        error: Exception,
        state_summary: str = "",
    ):
        """Log a node execution error with state context.

        Args:
            node_name: Name of the graph node that failed.
            error: The exception.
            state_summary: Short summary of the state when the error occurred.
        """
        logger.error(
            "[PEVR] Node '%s' failed: %s state=%s",
            node_name,
            error,
            state_summary,
            exc_info=True,
        )

    def log_config(self, config: Dict[str, Any]):
        """Record the PEVR configuration snapshot."""
        self.config = config
        logger.info(
            "[PEVR] Config: max_retries=%d max_steps=%d max_llm_calls=%d strictness=%s",
            config.get("max_retries", "?"),
            config.get("max_steps", "?"),
            config.get("max_llm_calls", "?"),
            config.get("strictness", "?"),
        )

    def log_routing(
        self,
        decision: Dict[str, Any],
        duration_ms: float,
        error: Exception = None,
        token_usage: Dict[str, int] = None,
    ):
        """记录 PERV Router 路由决策。

        Args:
            decision: RouteDecision 字典。
            duration_ms: 路由耗时（毫秒）。
            error: 可选的异常信息。
            token_usage: 可选的 token 使用量。
        """
        logger.info(
            "[PEVR] Routing decision: mode=%s risk=%s reason=%s source=%s (%.0fms)",
            decision.get("mode"),
            decision.get("risk"),
            decision.get("reason"),
            decision.get("source"),
            duration_ms,
        )
        if error:
            logger.warning("[PEVR] Routing error: %s", error)
        if token_usage:
            logger.debug(
                "[PEVR] Routing token usage: prompt=%d completion=%d",
                token_usage.get("prompt_tokens", 0),
                token_usage.get("completion_tokens", 0),
            )

    def log_custom(self, event_type: str, data: Any = None, **kwargs):
        """Record a custom event for learning integration tracking.

        Used by orchestrator pre-hooks and post-hooks to track pattern
        retrieval, strategy injection, semantic history search, and
        learning results.

        Args:
            event_type: Event category (e.g. "pattern_retrieval",
                "strategy_injection", "semantic_history", "learning_result").
            data: Optional data payload (dict, int, str, etc.).
            **kwargs: Additional key-value pairs merged into the event.
        """
        event: Dict[str, Any] = {
            "type": event_type,
            "timestamp": time.time(),
            "ts": _ts(),
        }
        if data is not None:
            event["data"] = data
        event.update(kwargs)
        self._custom_events.append(event)
        logger.debug(
            "[PEVR] Custom event: type=%s data=%s",
            event_type,
            str(data)[:100] if data is not None else "None",
        )


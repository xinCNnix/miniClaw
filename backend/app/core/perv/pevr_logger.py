    def save_trace(self, filepath: str):
        """Save full execution trace as JSON."""
        self._finalize_loop()
        path = Path(filepath)
        path.parent.mkdir(parents=True, exist_ok=True)
        with open(path, "w", encoding="utf-8") as f:
            json.dump(
                self.get_summary(),
                f,
                indent=2,
                ensure_ascii=False,
                default=str,
            )
        logger.info("[PEVR] Trace saved to %s", filepath)

    def _auto_save_trace(self):
        """Auto-save trace to logs/pevr_traces/ on session end."""
        try:
            settings = _get_settings()
            log_dir = getattr(settings, "log_dir", "logs")
            trace_dir = Path(log_dir) / "pevr_traces"
            trace_dir.mkdir(parents=True, exist_ok=True)

            import datetime
            ts = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
            sid = self.session_id[:8] if self.session_id else "no-session"
            filename = trace_dir / f"pevr_{ts}_{sid}.json"
            self.save_trace(str(filename))
        except Exception as e:
            logger.debug("[PEVR] Auto-save trace failed: %s", e)



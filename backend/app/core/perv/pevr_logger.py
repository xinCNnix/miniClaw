"""
PEVR Execution Lifecycle Logger

Captures the full Planner-Executor-Verifier-Replanner loop for debugging,
performance analysis, and error tracking.  Follows the same pattern as
``app.core.tot.tot_logger.ToTExecutionLogger``.

Usage::

    from app.core.perv.pevr_logger import PEVRLogger, get_pevr_logger

    pevr_log = get_pevr_logger(task="...", session_id="...")
    with pevr_log:
        pevr_log.log_planning(plan_steps=[...], duration_s=1.2)
        pevr_log.log_execution(tool_calls=[...], duration_s=3.4)
        pevr_log.log_verification(report={...}, duration_s=0.5)
        # ... loop continues ...
        pevr_log.log_finalization(answer="...", duration_s=0.8)
"""

import json
import logging
import time
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, List, Optional

from langchain_core.messages import BaseMessage

logger = logging.getLogger(__name__)


def _ts() -> str:
    """返回 ISO 格式的当前 UTC 时间戳（毫秒精度）。"""
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S.%f")[:-3] + "Z"


def extract_token_usage(response: Any) -> Dict[str, int]:
    """从 LLM 响应中提取 token 用量。

    兼容 LangChain 新旧两种 token 统计格式:
    - usage_metadata (LangChain 0.2+): input_tokens / output_tokens / total_tokens
    - response_metadata.token_usage (旧版): prompt_tokens / completion_tokens / total_tokens

    Args:
        response: LangChain AIMessage 或类似对象。

    Returns:
        包含 prompt_tokens, completion_tokens, total_tokens 的字典。
    """
    # 优先使用 usage_metadata (LangChain 0.2+)
    usage = getattr(response, "usage_metadata", None)
    if usage and isinstance(usage, dict):
        return {
            "prompt_tokens": usage.get("input_tokens", 0) or 0,
            "completion_tokens": usage.get("output_tokens", 0) or 0,
            "total_tokens": usage.get("total_tokens", 0) or 0,
        }

    # 回退到 response_metadata.token_usage
    meta = getattr(response, "response_metadata", {})
    if isinstance(meta, dict):
        token_usage = meta.get("token_usage", {})
        if isinstance(token_usage, dict) and token_usage:
            return {
                "prompt_tokens": token_usage.get("prompt_tokens", 0) or 0,
                "completion_tokens": token_usage.get("completion_tokens", 0) or 0,
                "total_tokens": token_usage.get("total_tokens", 0) or 0,
            }

    return {"prompt_tokens": 0, "completion_tokens": 0, "total_tokens": 0}

